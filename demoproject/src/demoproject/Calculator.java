package demoproject;

public class Calculator {

	public int add(int a, int b) {
		int r = a+b;
		return r ;	
		}
	
	public int subtract(int a, int b) {
		int r = a-b;
		return r ;
	}
	
	public int multiply(int a, int b) {
		int r = a*b;
		return r ;
	}
	
	public int division(int a, int b) {
		int r = a/b;
		return r ;
	}
	
	public int remainder(int a, int b) {
		int r = a%b;
		return r ;
	}
}
